    public void serch1(){    
        try{
             String query="select * from book where Accno='"+jtxtAccno.getText()+"'";
            rs=con.getQuery(query);
            if(rs.next()){
                String add2=rs.getString("Bname");
                jtxtname1.setText(add2);
                String add3=rs.getString("Bedition");
                jtxtedi.setText(add3);
                String add4=rs.getString("Bpublisher");
                jtxtpub.setText(add4);
                String add5=rs.getString("Bsection");
                jtxtSection.setText(add5);
                String add6=rs.getString("Bpages");
                jtxtpages.setText(add6);
                rs.close();
            }
            else{
                JOptionPane.showMessageDialog(null, "The book id not found");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                rs.close();

            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        
        
    }
