    public void search2(){
           try{
            String query="select * from student where regno='"+jtxtID.getText()+"'";
            rs=con.getQuery(query);
            if(rs.next()){
                String add2=rs.getString("sname");
                jtxtname.setText(add2);
                String add3=rs.getString("sbatch");
                jtxtbatch.setText(add3);
                String add4=rs.getString("sdep");
                jtxtdep.setText(add4);
                String add5=rs.getString("semail");
                jtxtemail.setText(add5);
                String add6=rs.getString("sphonenumber");
                jtxtcontactno.setText(add6);
                String add7=rs.getString("image");
                lblImage.setIcon(ResizeImage2(add7));
                rs.close();
            }
            else{
                JOptionPane.showMessageDialog(null, "The student's register number not found");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                rs.close();

            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
      public ImageIcon ResizeImage2(String imgPath){
        ImageIcon myImage=new ImageIcon(imgPath);
        Image img=myImage.getImage();
        Image newImage=img.getScaledInstance(lblImage.getWidth(),lblImage.getHeight(),Image.SCALE_SMOOTH);
        ImageIcon image=new ImageIcon(newImage);
        return image;
    }
