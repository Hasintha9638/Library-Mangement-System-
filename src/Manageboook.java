    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(jtxtAcc.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No data to update");
            return ;
        }
        int action=JOptionPane.showConfirmDialog(null,"Do you really want to update ", "Update", JOptionPane.YES_OPTION);
        if(action==00){
            try{
                Connect con=new Connect();
                String query="update book set Accno='"+jtxtAcc.getText()
                +"',Bname='"+jtxtname.getText()
                +"',Bedition ='"+jtxtedi.getText()
                +"',Bpublisher='"+jtxtpub.getText()
                +"',Bsection='"+jtxtsection.getText()
                +"',Bpages='"+jtxtpages.getText()+"' where Accno='"+ jtxtAcc.getText()+"' ";
               
                con.setQuery(query);

                JOptionPane.showMessageDialog(null, "Updated the data");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            jTable1();
            clear();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{
            Connect con =new Connect();
            int row=jTable1.getSelectedRow();
            String table_click=(jTable1.getModel().getValueAt(row,0).toString());
            String query=" select * from book where Accno='"+table_click+"' ";
           
            rs=con.getQuery(query);
            if(rs.next()){
                String add1=rs.getString("Accno");
                jtxtAcc.setText(add1);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jTable1MouseClicked
