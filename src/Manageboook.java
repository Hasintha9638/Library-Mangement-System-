    public void jTable1(){
            dtm=(DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
        try{
             Connect con =new Connect();
            String query="select Accno,Bname,Bedition,Bpublisher,Bsection,Bpages from book";
           rs=con.getQuery(query);
           while(rs.next()){
            String accno=rs.getString("Accno");
            String Bname=rs.getString("Bname");
            String Bedition=rs.getString("Bedition");
            String Bpublisher=rs.getString("Bpublisher");
            String Bsection=rs.getString("Bsection");
            String Bpages=rs.getString("Bpages");
            
            dtm.addRow(new Object[]{accno,Bname,Bedition,Bpublisher,Bsection,Bpages});
            
            
           }
            
        }catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
        }
        
    }
